function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

export function selectQuestions() {
  const good = shuffle(ALL_QUESTIONS.filter(q => q.type === 'good')).slice(0, 5)
  const bad = shuffle(ALL_QUESTIONS.filter(q => q.type === 'bad')).slice(0, 5)
  return shuffle([...good, ...bad])
}

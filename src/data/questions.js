function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

// 正解を常に配列として取り出す（通常: correct_answer 単数 / 上級: correct_answers 配列）
export function getCorrectIds(question) {
  return question.correct_answers ?? [question.correct_answer]
}

export function selectQuestions(level = 'normal') {
  const pool = level === 'advanced' ? ADVANCED_QUESTIONS : ALL_QUESTIONS
  const good = shuffle(pool.filter(q => q.type === 'good')).slice(0, 5)
  const bad = shuffle(pool.filter(q => q.type === 'bad')).slice(0, 5)
  return shuffle([...good, ...bad])
}
